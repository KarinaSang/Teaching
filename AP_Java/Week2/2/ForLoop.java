package week2;

public class ForLoop {

	public static void main(String[] args) {
		// Example 1
		for (int i = 1; i < 5; i++) {
			System.out.print(i + " ");
		}
		
		System.out.println();
		
		for (int k = 20; k >= 15; k--) {
			System.out.print(k + " ");
		}
		
		System.out.println();
		
		int j;
		for (j = 2; j <= 10; j+= 2) {
			System.out.print(j + " ");
		}
		

	}

}
